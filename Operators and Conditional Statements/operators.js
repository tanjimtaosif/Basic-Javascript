// Comparison operators - ( ==, !=, ===[equal to & type], !==[not equal to and type] )

let a = 5;
let b = 6;

console.log("5 == 2", a == b); // It will give a boolean value false
console.log("5! = S", a != b); // It will give a boolean value false
console.log("a == b:", a == b);     // true (compares value only)
console.log("a != b:", a != b);     // false (compares value only)
console.log("a === b:", a === b);   // false (compares value and type)
console.log("a !== b:", a !== b);   // true (compares value and type)
console.log("a > b:", a > b);     // false
console.log("a < b:", a < b);     // true
console.log("a >= b:", a >= b);   // false
console.log("a <= b:", a <= b);   // true

// Logical operators -> (AND [&&], OR [||], NOT [!])

console.log("a && b:", a && b);   // false (both must be true)
console.log("a || b:", a || b);   // true (at least one is true)
console.log("!a:", !a);           // false (NOT true = false)
console.log("!b:", !b);           // true (NOT false = true)